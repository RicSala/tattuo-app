/**
 * @param {string[]} labels - The labels to map to ids
 * @returns {string[]} The ids of the labels
 */
export const mapLabelsToIds = (labels) => {
  const styles = getStyleList();
  const labelToIdMap = styles.reduce((acc, style) => {
    acc[style.label] = style.id;
    return acc;
  }, {});

  return labels
    .map((label) => labelToIdMap[label])
    .filter((id) => id !== undefined);
};

export const mapValueToLabel = (value) => {
  const styles = getStyleList();
  const valueToLabelMap = styles.reduce((acc, style) => {
    acc[style.value] = style.label;
    return acc;
  }, {});

  return valueToLabelMap[value];
};
